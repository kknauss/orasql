SELECT PROGRAM_LOG_SEQ, PROGRAM_ID, JOB_NAME_STR, PROGRAM_START_TS, PROGRAM_END_TS, UPDATE_DATE, UPDATE_USER_STR, (PROGRAM_END_TS-PROGRAM_START_TS)
FROM DW_COMMON.PROGRAM_LOG
WHERE UPDATE_DATE > (SYSDATE -14)
AND PROGRAM_ID like '%FLAT%'
ORDER BY UPDATE_DATE DESC;

SELECT *
FROM DW_COMMON.PROGRAM_ACTIVITY_LOG
WHERE PROGRAM_LOG_SEQ = 62448
ORDER BY CREATE_TS DESC;

SELECT *
FROM DW_COMMON.PROGRAM_DEBUG_LOG
WHERE DEBUG_DATE > (SYSDATE - 3)
AND PROGRAM_LOG_SEQ = 62448
ORDER BY UPDATE_DATE DESC;
