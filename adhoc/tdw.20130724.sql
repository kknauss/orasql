SELECT LOG_ID, LOG_DATE, OWNER, JOB_NAME, JOB_SUBNAME, OPERATION, STATUS, ADDITIONAL_INFO
FROM DBA_SCHEDULER_JOB_LOG 
WHERE JOB_NAME = 'FLAT_ODS_LOAD_RUN' AND LOG_DATE > (SYSDATE - 2)
ORDER BY LOG_DATE DESC;

